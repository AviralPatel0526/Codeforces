import java.util.*;
import java.lang.*;
import java.io.*;
/**
 * B_Cat_Fox_and_the_Lonely_Array
 */
public class B_Cat_Fox_and_the_Lonely_Array {

    public static void main(String[] args) throws java.lang.Exception {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t != 0) {
            t--;
            System.out.println();
        }
    }
}