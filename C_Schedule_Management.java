    static class FastReader {
        BufferedReader br;
        StringTokenizer st;
    
        public FastReader() {
            br = new BufferedReader(new InputStreamReader(System.in));
        }
    
        String next() {
            while (st == null || !st.hasMoreTokens()) {
                try {
                    st = new StringTokenizer(br.readLine());
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            return st.nextToken();
        }
    
        int nextInt() {
            return Integer.parseInt(next());
        }
    
        long nextLong() {
            return Long.parseLong(next());
        }
    
        double nextDouble() {
            return Double.parseDouble(next());
        }
    
        String nextLine() {
            String str = "";
            try {
                str = br.readLine().trim();
            } catch (Exception e) {
                e.printStackTrace();
            }
            return str;
        }
    }
    static boolean check(int n, int m, int t, HashMap<Integer, Integer> map) {
        long free = 0, need = 0;
        for (int i = 1; i <= n; i++) {
            int cnt = map.getOrDefault(i, 0);
            if (t >= cnt) {
                free += (t - cnt) / 2; // Calculate free time available
            } else {
                need += (cnt - t); // Calculate additional tasks needed
            }
        }
        return need <= free; // Check if free time can cover the need
    }

    public static void main(String[] args) throws java.lang.Exception {
        // Scanner in = new Scanner(System.in);
        int t = in.nextInt();

        while (t-- > 0) {
            int n = in.nextInt();
            int m = in.nextInt();
            int[] a = new int[m];
            for (int i = 0; i < m; i++) {
                a[i] = in.nextInt();
            }

            HashMap<Integer, Integer> map = new HashMap<>();
            for (int i = 0; i < m; i++) {
                map.put(a[i], map.getOrDefault(a[i], 0) + 1);
            }

            int l = 0, r = 2 * m, ans = -1;
            while (l <= r) {
                int mid = (l + r) / 2;
                if (check(n, m, mid, map)) {
                    ans = mid;
                    r = mid - 1;
                } else {
                    l = mid + 1;
                }
            }
            System.out.println(ans);
        }
        // in.close();
    }
